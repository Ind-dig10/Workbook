using System;
using System.Collections.Generic;
using System.Configuration;
using System.Data;
using System.Globalization;
using System.Linq;
using System.Threading;
using System.Threading.Tasks;
using System.Windows;
using System.Windows.Markup;

namespace todoApp
{
    /// <summary>
    /// Логика взаимодействия для App.xaml
    /// </summary>
    public partial class App : Application
    {
        protected override void OnStartup(StartupEventArgs e)
        {
            var cultireInfo = new CultureInfo("ru-RU");
            Thread.CurrentThread.CurrentCulture = cultireInfo;
            Thread.CurrentThread.CurrentUICulture = cultireInfo;
            CultureInfo.DefaultThreadCurrentCulture = cultireInfo;
            CultureInfo.DefaultThreadCurrentUICulture = cultireInfo;
            FrameworkElement.LanguageProperty.OverrideMetadata(typeof(FrameworkElement),
                new FrameworkPropertyMetadata(XmlLanguage.GetLanguage(CultureInfo.CurrentCulture.IetfLanguageTag)));
            base.OnStartup(e);
        }
    }
}
